import fs from 'fs';
import path from 'path';

function getImportSource(importBlock) {
  const fromMatch = importBlock.match(/\bfrom\s+['"]([^'"]+)['"]/);
  if (fromMatch) {
    return fromMatch[1];
  }

  const sideEffectMatch = importBlock.match(/^\s*import\s+['"]([^'"]+)['"]/m);
  return sideEffectMatch?.[1] ?? null;
}

function stripImportBlocks(content, shouldStripSource) {
  const lines = content.split('\n');
  const cleaned = [];
  let importBlock = null;

  for (const line of lines) {
    const trimmed = line.trim();

    if (importBlock) {
      importBlock.push(line);
      if (trimmed.endsWith(';')) {
        const source = getImportSource(importBlock.join('\n'));
        if (!source || !shouldStripSource(source)) {
          cleaned.push(...importBlock);
        }
        importBlock = null;
      }
      continue;
    }

    if (trimmed.startsWith('import ')) {
      if (trimmed.endsWith(';')) {
        const source = getImportSource(line);
        if (!source || !shouldStripSource(source)) {
          cleaned.push(line);
        }
      } else {
        importBlock = [line];
      }
      continue;
    }

    cleaned.push(line);
  }

  if (importBlock) {
    cleaned.push(...importBlock);
  }

  return cleaned.join('\n');
}

// 1. Compile schema.prisma
const prismaSchemaDir = 'apps/api/prisma/schema';
const prismaSchemaOut = 'apps/api/prisma/schema.prisma';

if (fs.existsSync(prismaSchemaDir)) {
  const files = fs.readdirSync(prismaSchemaDir);
  // Sort files so main.prisma is first, enums.prisma is second, then the rest alphabetically
  files.sort((a, b) => {
    if (a === 'main.prisma') return -1;
    if (b === 'main.prisma') return 1;
    if (a === 'enums.prisma') return -1;
    if (b === 'enums.prisma') return 1;
    return a.localeCompare(b);
  });

  let combinedSchema = `// ─────────────────────────────────────────────────────────────────────────────
// WARNING: This file is AUTO-GENERATED from the files in apps/api/prisma/schema/.
// Do NOT edit this file directly. Edit the split files and run compile:artifacts.
// ─────────────────────────────────────────────────────────────────────────────

`;

  for (const file of files) {
    if (file.endsWith('.prisma')) {
      let fileContent = fs.readFileSync(
        path.join(prismaSchemaDir, file),
        'utf8',
      );
      // For schema.prisma, we must remove the previewFeatures line if it's there
      if (file === 'main.prisma') {
        fileContent = fileContent.replace(
          '  previewFeatures = ["prismaSchemaFolder"]\n',
          '',
        );
      }
      combinedSchema += `// ─── Compiled from schema/${file} ───\n${fileContent}\n\n`;
    }
  }

  fs.writeFileSync(prismaSchemaOut, `${combinedSchema.trimEnd()}\n`, 'utf8');
  console.log(`Compiled Prisma schema to: ${prismaSchemaOut}`);
}

// 2. Compile permissions.ts
const permsDir = 'packages/core/src/permissions';
const permsOut = 'packages/core/src/permissions.ts';

if (fs.existsSync(permsDir)) {
  // Compile catalog.ts first from its split files
  const catalogDir = path.join(permsDir, 'catalog');
  const catalogOut = path.join(permsDir, 'catalog.ts');
  if (fs.existsSync(catalogDir)) {
    const files = fs
      .readdirSync(catalogDir)
      .filter((file) => file.endsWith('.ts'));
    files.sort();

    let combinedItems = '';
    for (const file of files) {
      const content = fs.readFileSync(path.join(catalogDir, file), 'utf8');
      const startIdx = content.indexOf('[');
      const endIdx = content.lastIndexOf(']');
      if (startIdx !== -1 && endIdx !== -1) {
        let innerContent = content.substring(startIdx + 1, endIdx).trim();
        if (innerContent.endsWith(',')) {
          innerContent = innerContent.slice(0, -1).trim();
        }
        if (innerContent) {
          combinedItems += `  // ─── Compiled from catalog/${file} ───\n  ${innerContent},\n\n`;
        }
      }
    }

    const catalogFileContent = `// ─────────────────────────────────────────────────────────────────────────────
// WARNING: This file is AUTO-GENERATED from the files in packages/core/src/permissions/catalog/.
// Do NOT edit this file directly. Edit the split files and run compile:artifacts.
// ─────────────────────────────────────────────────────────────────────────────

export const permissionCatalog = [
${combinedItems.trimEnd()}
] as const;

export type PermissionResource = (typeof permissionCatalog)[number]["resource"];
export type PermissionAction = (typeof permissionCatalog)[number]["action"];
export type PermissionKey =
  \`\${(typeof permissionCatalog)[number]["resource"]}:\${(typeof permissionCatalog)[number]["action"]}\`;
`;

    fs.writeFileSync(catalogOut, catalogFileContent, 'utf8');
    console.log(`Compiled permission catalog to: ${catalogOut}`);
  }

  const catalogContent = fs.readFileSync(
    path.join(permsDir, 'catalog.ts'),
    'utf8',
  );
  const utilsContent = fs.readFileSync(path.join(permsDir, 'utils.ts'), 'utf8');
  const rolesContent = fs.readFileSync(path.join(permsDir, 'roles.ts'), 'utf8');

  // Strip relative imports from rolesContent
  const cleanedRoles = stripImportBlocks(rolesContent, (source) =>
    source.startsWith('./'),
  );

  // Combine them
  const combinedPerms = `// ─────────────────────────────────────────────────────────────────────────────
// WARNING: This file is AUTO-GENERATED from the files in packages/core/src/permissions/.
// Do NOT edit this file directly. Edit the split files and run compile:artifacts.
// ─────────────────────────────────────────────────────────────────────────────

${catalogContent}

${utilsContent}

${cleanedRoles}
`;

  fs.writeFileSync(permsOut, combinedPerms, 'utf8');
  console.log(`Compiled permissions to: ${permsOut}`);
}

// 3. Compile types.ts
const typesDir = 'packages/core/src/types';
const typesOut = 'packages/core/src/types.ts';

if (fs.existsSync(typesDir)) {
  const files = fs.readdirSync(typesDir);
  // Sort files so common.ts is first, then the rest alphabetically
  files.sort((a, b) => {
    if (a === 'common.ts') return -1;
    if (b === 'common.ts') return 1;
    return a.localeCompare(b);
  });

  let combinedTypes = `// ─────────────────────────────────────────────────────────────────────────────
// WARNING: This file is AUTO-GENERATED from the files in packages/core/src/types/.
// Do NOT edit this file directly. Edit the split files and run compile:artifacts.
// ─────────────────────────────────────────────────────────────────────────────

import type { PermissionKey } from './permissions.js';

`;

  for (const file of files) {
    if (file.endsWith('.ts')) {
      const fileContent = fs.readFileSync(path.join(typesDir, file), 'utf8');

      // Clean relative imports (e.g. import type { ... } from './...')
      const cleanedContent = stripImportBlocks(
        fileContent,
        (source) =>
          source.startsWith('./') ||
          source.startsWith('../permissions') ||
          source.startsWith('../payroll'),
      );

      combinedTypes += `// ─── Compiled from types/${file} ───\n${cleanedContent}\n\n`;
    }
  }

  fs.writeFileSync(typesOut, combinedTypes, 'utf8');
  console.log(`Compiled types to: ${typesOut}`);
}

// 4. Compile validation.ts
const valDir = 'packages/core/src/validation';
const valOut = 'packages/core/src/validation.ts';

if (fs.existsSync(valDir)) {
  const files = fs.readdirSync(valDir);
  // Sort files with custom order (auth and student first) to prevent hoisting issues
  files.sort((a, b) => {
    const order = [
      'auth.ts',
      'contact-profile.ts',
      'student.ts',
      'academic.ts',
    ];
    const idxA = order.indexOf(a);
    const idxB = order.indexOf(b);
    if (idxA !== -1 && idxB !== -1) return idxA - idxB;
    if (idxA !== -1) return -1;
    if (idxB !== -1) return 1;
    return a.localeCompare(b);
  });

  let combinedVal = `// ─────────────────────────────────────────────────────────────────────────────
// WARNING: This file is AUTO-GENERATED from the files in packages/core/src/validation/.
// Do NOT edit this file directly. Edit the split files and run compile:artifacts.
// ─────────────────────────────────────────────────────────────────────────────

import { z } from 'zod';

`;

  for (const file of files) {
    if (file.endsWith('.ts')) {
      const fileContent = fs.readFileSync(path.join(valDir, file), 'utf8');

      // Clean Zod imports and local schema file imports
      const cleanedContent = stripImportBlocks(
        fileContent,
        (source) => source === 'zod' || source.startsWith('./'),
      );

      combinedVal += `// ─── Compiled from validation/${file} ───\n${cleanedContent}\n\n`;
    }
  }

  fs.writeFileSync(valOut, combinedVal, 'utf8');
  console.log(`Compiled validation to: ${valOut}`);
}
